void object::raccelerate(const int& index, const float& shift)
{
	if (isinf(shift))
		return;
	if (shift == 0.0f)
		return;
	speedFactor[index] += shift;
	return;
}


const float& object::getDir()
{
	return _movementAngle;
}

void object::adjustDir(const float& angle)
{
	while (_lookAngle >= 360.0f)	_lookAngle -= 360.0f;
	while (_lookAngle <= -360.0f)	_lookAngle += 360.0f;
	_lookAngle += angle;
	while (_lookAngle >= 360.0f)	_lookAngle -= 360.0f;
	while (_lookAngle <= -360.0f)	_lookAngle += 360.0f;
	sinval = sinf(toRAD(_lookAngle));
	cosval = cosf(toRAD(_lookAngle));


	return;
}


void object::rotateAround(f2 point, float force)
{
	cord.x -= point.x;
	cord.y -= point.y;

	float sink, cosk;

	sink = sinf(toRAD(force));
	cosk = cosf(toRAD(force));

	float kx, ky;

	kx = (cord.x * cosk) - (cord.y * sink);
	ky = (cord.x * sink) + (cord.y * cosk);

	cord.x = kx;
	cord.y = ky;

	cord.x += point.x;
	cord.y += point.y;

}




void object::setDir(const float& angle)
{

	_lookAngle = angle;

	//rotate(angle - _lookAngle, x, y);
	_movementAngle = angle;
	sinval = sinf(toRAD(_movementAngle));
	cosval = cosf(toRAD(_movementAngle));

	return;
}

ibig object::examineOther(object* other)
{
	decimal typ = type();
	if (typ == other->type())
		return (topinions[typ][typ] + SAME_TYPE_OFFSET);

	return (topinions[typ][other->type()]);
}

